
// Verificar se número é PAR ou ÍMPAR


public class ifExe {

	public static void main(String[] args) {
		
		int numero = 74;
		
		if (numero % 2 == 0) {
			
			System.out.println("O número: " + numero + " é PAR");
			
		} else {
			
			System.out.println("O número: " + numero + " é ÍMPAR");
		
		}
	}
}


