
public class doWhile {

	public static void main(String[] args) {

		int i = 0;

		do {
						
			System.out.println(i);			
			i++;
			
		} while (i < 10);
		
			
		
	}
}


// doWhile vai imprmir a primeira condição, sendo true ou false.

