package atividade1;


public class atividade1Exe2 {

	public static void main(String[] args) {
		
		int numeroInteiro = 10;
		int numeroSucessor = numeroInteiro + 1;
		int numeroAntecessor = numeroInteiro - 1;
		
		System.out.println("O número inteiro é: " + numeroInteiro + ", o seu sucessor é: " + numeroSucessor + ", e o seu antecessor é: " + numeroAntecessor);
		

	}

}
