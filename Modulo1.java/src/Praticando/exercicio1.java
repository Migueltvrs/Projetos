// Faça um programa que imprima os números de 1 a 100 utilizando o laço for. 

package Praticando;

public class exercicio1 {

	public static void main(String[] args) {


		for (int i = 1; i < 101; i++) {
			System.out.println(i);
		}
		int exe = 11 / 2;
		System.out.println(exe);
	}

}

